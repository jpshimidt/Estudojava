public class exercicio1Lista {
    public static void main(String[] args) {
    
        Integer[] numeros = new Integer[] {10, 15, 1000, 30, 50};

        for(int i = 0; i < numeros.length; i++){
            System.out.println(numeros[i]);
        }

    }
}
